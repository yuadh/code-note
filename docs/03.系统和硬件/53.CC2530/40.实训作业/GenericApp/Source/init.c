void Init_32MClock(){
  //初始化时钟函数
  CLKCONCMD &=~0x40;//0100 0000系统时钟设置为32M
  while(CLKCONSTA&0x40); //等待时钟切换
  CLKCONCMD &=~0x07;    //0000 0111当前时钟速率32M
}
void Init_16MClock(){
  //初始化时钟函数
  CLKCONCMD |= 0x40;
  while(!(CLKCONSTA & 0x40));
  CLKCONCMD |= 0x01;
}

/*======================初始化串口0函数=======================*/
void Init_Uart0()
{
 //1-指定串口的IO位置；
   PERCFG = 0x00;    //0000 0001 UART0,备用位置1
 //2-相应IO配置成片上外设功能；
   P0SEL = 0x0C;   //0000 1100 P0_2,P0_3为片上外设
 //3-选择串口工作方式（异步UART模式，8个数据位、1个停止位、无流控、无校验确立）。
   U0CSR |=0xc0;  //1100 0000 设置为UART模式，接收使能
   U0UCR |=0x80;  //1000 0000  当前为空闲状态
 //4-波特率；
   U0GCR =8;
   U0BAUD =59;
 //5-清除发送、接收标志位
   UTX0IF =0;
   URX0IF =0;
//6-开CPU中断、对应串口接收中断；
   URX0IE =1;//接收中断使能
   EA=1;
}
