//****输出端口初始化
void init_port(){
  //初始化输出端口
  //初始化LED灯的I/O端口
  P1SEL &= ~0x1b;   //P1_0、P1_1、P1_3和P1_4作为通用I/O端口
  P1DIR |= 0x1b;    //P1_0、P1_1、P1_3和P1_4端口输出
  //关闭所有的LED灯
  P1 &= ~0x1b;
  P1SEL &= ~0x04;     //P1_2作为通用I/O端口
  P1DIR &= ~0x04;     //P1_2端口输入
  P1INP &= ~0x04;     //P1_2设置为上拉/下拉模式
  P2INP &= ~0x40;     //P1_2设置为上拉

  P0SEL &= ~0x02;     //P0_1作为通用I/O端口
  P0DIR &= ~0x02;     //P0_1端口输入
  P0INP &= ~0x02;     //P0_1设置为上拉/下拉模式
  P2INP &= ~0x20;     //P0_1设置为上拉
}
//****中断 sw_1 sw_2 初始化
void init_btn(int sta){ //设置上升沿还是下降沿  0是上升沿 1是下降沿
  //对SW1的中断使能
  IEN2 |= 0x10;     //使能P1端口组外部中断（IEN2不能位操作）0001 0000
  P1IEN |= 0x04;    //使能P1_2引脚外部中断 0000 0100
  //对SW2的中断使能 0010 0000
  IEN1 |= 0x20; //是P0端口组
  P0IEN |= 0x02;  //0000 0010
  if(sta){//下降沿触发
    PICTL |= 0x02;   //配置P1_0到P1_3上升沿触发 0010
    PICTL |= 0X01; //设置上升沿触发
  }else{//上升沿触发
    PICTL &= ~0x02;   //配置P1_0到P1_3上升沿触发 0010
    PICTL &= ~0X01; //设置上升沿触发
  }
  EA = 1;       //打开总中断
}
// ****延时函数
void Delay(unsigned int t)
{
  int i;
  while(t--){
    for(i=0;i<255;i++);
  }
}
void Init_WDT(int sta)
{
  if(sta){
    WDCTL |= 0x0c;  //0000 1100
    IEN2 |= 0x20;  //开启看门狗中断
    EA = 1;//开启总中断
  }else{
    WDCTL |= 0x08;  //看门狗模式 时间间隔为1s
  }
}
